#ifndef _TYPES_H_
#define _TYPES_H_

 /* заголовочный файл прееопределений стандартных типов */

#define XDATA __xdata

#define VOID void

/* 8 bit types */
#define BYTE unsigned char
#define SBYTE signed char

/* 16 bit types */
#define SHORT int
#define USHORT unsigned int 

/* 32 bit types */
#define INT long 
#define UINT unsigned long 

/* float-point */
#define FLOAT float 
#define DOUBLE double 



#endif /* _TYPES_H_ */